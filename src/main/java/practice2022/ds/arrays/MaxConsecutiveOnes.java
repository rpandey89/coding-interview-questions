    private static int findMaxConsecutiveOnes(int[] nums) {
        int maxOnes = Integer.MIN_VALUE;
        int start = 0;
        int end = 0;
        int currentOnes = 0;
        int zeroInstance = 0;
        while (end < nums.length) {

            while(end < nums.length && zeroInstance <= 1) {
                if(nums[end] == 0) {
                    zeroInstance++;
                    if (zeroInstance == 2) {
                       // end++;
                        break;
                    }
                }
                currentOnes++;
                end++;
                maxOnes = Math.max(maxOnes, currentOnes);
            }

            while(zeroInstance > 1) {
                if (nums[start] == 0) {
                    zeroInstance--;
                }
                start++;
                currentOnes--;
            }
            zeroInstance = 0;
        }
        return maxOnes;
    }

    private static void printArray(int[] nums) {
        IntStream
                .range(0, nums.length)
                .forEach(i -> {
                    if (i == 0) {
                        System.out.print("[ ");
                    }
                    System.out.print(nums[i]);
                    if (i < nums.length - 1) {
                        System.out.print(", ");
                    } else {
                        System.out.print("]\n");
                    }

                });

    }

